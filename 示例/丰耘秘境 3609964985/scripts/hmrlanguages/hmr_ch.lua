local function OverrideCharacterString(...)
    local data = {...}
    local newValue = data[#data]
    table.remove(data, #data)

    for k, v in pairs(STRINGS.CHARACTERS) do
        local pointer = v
        for i = 1, #data - 1 do
            if pointer[data[i]] == nil then
                pointer[data[i]] = {}
            end
            pointer = pointer[data[i]]
            if pointer == nil or type(pointer) ~= "table" then
                break
            end
        end
        if pointer ~= nil and type(pointer) == "table" then
            pointer[data[#data]] = newValue
        end
    end
end

